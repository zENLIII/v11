import Link from "next/link"
import { Button } from "@/components/ui/button"
import { useParams } from 'next/navigation';
import { useEffect, useState } from 'react';
import { db } from '@/lib/firebase';
import { doc, getDoc } from 'firebase/firestore';

export function MyProjectLearning() {
  const { id } = useParams();
  const [project, setProject] = useState(null);

  useEffect(() => {
      if (id) {
          fetchProject(id);
      }
  }, [id]);

  const fetchProject = async (projectId) => {
      const docRef = doc(db, "projects", projectId);
      const docSnap = await getDoc(docRef);
      if (docSnap.exists()) {
          setProject(docSnap.data());
      }
  };

  if (!project) {
    return <p>Loading...</p>;
  }
    
  return (
    
    <div className="grid min-h-screen w-full lg:grid-cols-[280px_1fr]">
      <div className="flex flex-col">
        
        <main className="grid gap-4 p-4 md:gap-8 md:p-10">
        <div className="grid md:grid-cols-[1fr_300px] gap-6 items-start max-w-5xl mx-auto lg:grid-cols-[1fr_400px] xl:grid-cols-[1fr_500px]">
      <div className="space-y-4">
        <div className="space-y-2">
          <h1 className="text-3xl font-bold tracking-tighter sm:text-5xl">{project.name}</h1>
          <p className="text-gray-500 dark:text-gray-400">Mentored by {project.mentor}</p>
          <p className="text-gray-500 dark:text-gray-400">
            Welcome to the {project.name}! 
            {project.intro}
          </p>
        </div>
      </div>
      <div>
        <div className="space-y-2">
          <h2 className="text-2xl font-bold tracking-tighter sm:text-4xl">Latest Announcements</h2>
          <div className="grid gap-2">
            <div className="flex items-start gap-4">
              <CalendarIcon className="w-6 h-6 flex-shrink-0" />
              <div className="space-y-1.5">
                <h3 className="font-bold">Welcome to the Course!</h3>
                <p className="text-sm text-gray-500 dark:text-gray-400">Posted on: September 10, 2023</p>
              </div>
            </div>
            
          </div>
        </div>
      </div>
      </div>
    
      </main>
      </div>
    </div>



  )
}


